	package KelasObjekSesi4;
	
	public class Gelas {
		// Atribut untuk menyimpan warna gelas
		    private String warna;
		 
		    // Konstruktor untuk menginisialisasi warna gelas
		    public Gelas(String w) {
		        warna = w;
		    }
	
		 // Setter untuk mengubah warna gelas
		    void setWarna(String w) {
		        warna = w;
		    }
	
		 // Getter untuk mendapatkan warna gelas
		    String getWarna() {
		        return warna;
		    }
		}
	
